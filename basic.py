#Q. Consider a character input if it is uppercase convert it into lower case it into lower
#   case, if it is lower convert it into upper case, if it is digit print the reminder when it
#   is divied by 3 else if it is special charcter print it's ASCII value

a=(input('Enter the character: '))
if 'A' <= a <= 'Z':
    print(a.swapcase(),'Upper to lower')
elif 'a' <= a <= 'z':
    print(a.swapcase(),'Lower to upper')
elif '0' <= a <= "9":
    print(int(a)%3,'Reminder of digit')
else:
    print(ord(a),'ASCII value')



